// FeatureCard component
interface FeatureCardProps {
  icon: React.ReactNode;
  title: string;
  description: string | JSX.Element;
  glow?: boolean;
}

const FeatureCard = ({ icon, title, description, glow }: FeatureCardProps) => {
  return (
    <div className="relative">
      {glow && (
        <div
          className="absolute -inset-1 opacity-30 blur-lg rotate-180 z-0"
          style={{
            background:
              "linear-gradient(90deg, #44ff9a -0.55%, #44b0ff 22.86%, #8b44ff 48.36%, #ff6644 73.33%, #ebff70 99.34%)",
          }}
        ></div>
      )}
      <Card className="relative z-10 overflow-hidden shadow-md rounded-xl bg-white border border-gray-200 text-black">
        <CardContent className="p-9 space-y-6 text-center sm:text-left">
          <div className="mx-auto sm:mx-0 w-12 h-12 text-black">{icon}</div>
          <h3 className="text-2xl font-bold text-black">{title}</h3>
          <p className="text-base text-black">{description}</p>
        </CardContent>
      </Card>
    </div>
  );
};

export { FeatureCard };
